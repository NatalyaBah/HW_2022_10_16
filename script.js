// 9. Написать функцию, которая принимает количество секунд, переводит их в часы, минуты и секунды и возвращает в
// виде строки «чч:мм:сс».

function hour(sec) {
	let sec1 = sec % 60;
	let min = (sec - sec1) / 60;
	let min1 = min % 60;
	let hours = (min - min1) / 60;
	return `${times(hours)} : ${times(min1)} : ${times(sec1)}`;
}
console.log("Задание №9 : " + hour(7230));

