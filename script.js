// совершенное число или нет.

function rangePerfectNumber(min, max = 0) {
	let perfectNum = 0;
	for (let i = min; i <= max; i++) {
		if (perfectNumber(i)) {
			perfectNum += i + `,`;
		}
	}
	return perfectNum;
}
